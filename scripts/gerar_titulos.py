COR_TITULO = "\033[33m"
COR_LOCAL  = "\033[36m"
RESET      = "\033[0m"

def texto_para_ascii(texto: str) -> list:
    linhas = ["", "", "", "", "", ""]
    for char in texto.upper():
        bloco = LETRAS.get(char, LETRAS[' '])
        for i in range(6):
            linhas[i] += bloco[i] + " "
    return linhas

def gerar_titulos(caminho_entrada: str, caminho_saida: str):
    with open(caminho_entrada, "r", encoding="utf-8") as f:
        dados = json.load(f)

    for local in dados["locais"]:
        nome = local["nome"]
        ascii_art = texto_para_ascii(nome)
        local["titulo"] = ascii_art
        print(f"{COR_LOCAL}✔ {nome}{RESET}")
        for linha in ascii_art:
            print(f"  {COR_TITULO}{linha}{RESET}")
        print()

    with open(caminho_saida, "w", encoding="utf-8") as f:
        json.dump(dados, f, ensure_ascii=False, indent=4)

    print(f"{COR_LOCAL}Arquivo salvo em: {caminho_saida}{RESET}")

