# FunГ§Гөes para centralizar e formatar o texto
def visibleLen(s: str) -> int:
    return len(re.sub(r'\033\[[0-9;]*m', '', s))

def padCenter(s: str, width: int) -> str:
    pad = width - visibleLen(s)
    left = pad // 2
    right = pad - left
    return ' ' * left + s + ' ' * right

def padLeft(s: str, width: int) -> str:
    pad = width - visibleLen(s)
    return s + ' ' * max(0, pad)

